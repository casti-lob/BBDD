--3
SELECT a.NOMBRE 
FROM ASIGNATURA a , PROFESOR p , PERSONA p2 
WHERE a.IDPROFESOR= p.IDPROFESOR AND p.DNI = p2.DNI AND upper(p2.CIUDAD) LIKE 'SEVILLA'; 

--4
